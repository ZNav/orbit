# You may have specific operators defined. For now, let's assume you have basic functionality for them:
def add(x, y):
    return x + y

def subtract(x, y):
    return x - y

def multiply(x, y):
    return x * y

def divide(x, y):
    if y == 0:
        return 'Error: Division by Zero'
    return x / y

def bitwise_or(x, y):
    return x | y

def bitwise_and(x, y):
    return x & y

def bitwise_xor(x, y):
    return x ^ y

